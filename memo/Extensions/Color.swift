    static let memoBlue            = UIColor(named: "memo_blue")!
    static let memoRed             = UIColor(named: "memo_red")!
    static let memoGreen           = UIColor(named: "memo_green")!
    static let memoYellow          = UIColor(named: "memo_yellow")!
    // Uncolored
    static let memoBlack           = UIColor(named: "memo_black")!
    static let memoGray            = UIColor(named: "memo_gray")!
    static let memoLightGray       = UIColor(named: "memo_lightGray")!
    static let memoWhite           = UIColor(named: "memo_white")!
    // DarkMode
    static let memoBackground      = UIColor(named: "memo_background")!
    static let memoLightBackground = UIColor(named: "memo_lightBackground")!
    static let memoText            = UIColor(named: "memo_text")!
    static let memoTextBackground  = UIColor(named: "memo_textBackground")!
}
